#include<iostream>
using namespace std;

int main() {
	// increment & decrement
	int a, b;
	
	// post increment
	a = 1;
	cout << a++ << endl; // menampilkan value a awal
	cout << a++ << endl;
	cout << a << endl;
	
	// pemisah
	cout << "========================" << endl;
	// pemisah
	
	// pre increment
	b = 1;
	cout << ++b << endl; // melakukan operasi terlebih dahulu 
	cout << ++b << endl;
	cout << b << endl;
	
	// pemisah
	cout << "========================" << endl;
	// pemisah
	
	// increment & decrement
	int x, y;
	
	// post decrement
	x = 5;
	cout << x-- << endl; // menampilkan value a awal
	cout << x-- << endl;
	cout << x << endl;
	
	// pemisah
	cout << "========================" << endl;
	// pemisah
	
	// pre increment
	y = 5;
	cout << --y << endl; // melakukan operasi terlebih dahulu 
	cout << --y << endl;
	cout << y << endl;
}

















