#include <iostream>
#include <cmath>

int main() {
	// pow (pangkat)
		double a = pow(9, 2); 
		// (9, 2) parameter yang ke-2 yang berisi 2 adalah pangkat
		std::cout << "\n\tpow \t: " << a << std::endl;
		
	// sqrt (akar)
		double b = sqrt(81);
		// akar dari 81 adalah 9
		std::cout << "\n\tsqrt \t: " << b << std::endl;
		
	return 0;
}
